public class literals{
    public static void main(String[] args){
        byte age=34;
        char ch='A';
        int  x=9;
        long b=45l;//by default long takes int
        short u=43;
        float f1=5.6f;//floating point numbers
        double d1=4.66d;
        System.out.print(age + " " + ch + " " + x + " " + b + " " + u + " " + f1 + " " + d1);
    }
}